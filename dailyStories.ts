export function getDailyItems(list: any[], count: number) {
  const t = new Date();
  const seedStr = `${t.getFullYear()}-${t.getMonth()}-${t.getDate()}`;
  let hash = 0;
  for (let i = 0; i < seedStr.length; i++) {
    hash = Math.imul(31, hash) + seedStr.charCodeAt(i) | 0;
  }
  const randCount = count + (Math.abs(hash) % 3); 
  const copy = [...list];
  let m = copy.length, temp, i;
  let currHash = hash;
  while (m) {
    currHash = Math.imul(31, currHash) + 1 | 0;
    i = Math.abs(currHash) % m--;
    temp = copy[m];
    copy[m] = copy[i];
    copy[i] = temp;
  }
  return copy.slice(0, randCount);
}
